// The initialization sequence for the SSD1315
static const uint8_t ssd1315_init_sequence[] = {
	0x00,			// Send commands
	0xAE,			// Set Display ON/OFF - AE=OFF, AF=ON
	0xD5, 0xf0,		// Set display clock divide ratio/oscillator frequency, set divide ratio
	0xA8, 0x3F,		// Set multiplex ratio (1 to 64) ... (height - 1)
	0xD3, 0x00,		// Set display offset. 00 = no offset
	0x40 | 0x00,	// Set start line address, at 0.
	0x8D, 0x14,		// Charge Pump Setting, 14h = Enable Charge Pump
	0x20, 0x00,		// Set Memory Addressing Mode - 00=Horizontal, 01=Vertical, 10=Page, 11=Invalid
	0xA0 | 0x00,	// Set Segment Re-map
	0xC0 | 0x00,	// Set COM Output Scan Direction
	0xDA, 0x12,		// Set COM Pins Hardware Configuration - 128x32:0x02, 128x64:0x12
	0x81, 0x80,		// Set contrast control register
	0xD9, 0x22,		// Set pre-charge period (0x22 or 0xF1)
	0xDB, 0x20,		// Set Vcomh Deselect Level - 0x00: 0.65 x VCC, 0x20: 0.77 x VCC (RESET), 0x30: 0.83 x VCC
	0xA4,			// Entire Display ON (resume) - output RAM to display
	0xA6,			// Set Normal/Inverse Display mode. A6=Normal; A7=Inverse
	0x2E,			// Deactivate Scroll command
	0xAF,			// Set Display ON/OFF - AE=OFF, AF=ON
};

// Set the "cursor" to x,y where y is the PAGE NUMBER
ssd1315_status_t ssd1315_setpos(uint8_t x, uint8_t y) {

	// Set the position to x, y
	// Note y is PAGE NUMBER
	uint8_t buf[] = {0x00,
			  		 0xb0 | (y & 0x07),
					 0x10 | ((x & 0xf0) >> 4),
					 x & 0x0f
					};
	return (i2c1_transmit(SSD1315_ADDR, buf, sizeof buf) == 0) ? SSD1315_OK : SSD1315_ERR;

}

// Fill the screen with a pattern, 0x00 = all off, 0xff = all on
ssd1315_status_t ssd1315_fillscreen(uint8_t fill) {

	// We tested it, a data-command byte and then 128 data bytes
	uint8_t buf[128+1];
	buf[0] = 0x40; // Send data

	// Fill buffer, 129 bytes
	for (int i = 1; i <= 128; i++) {
		buf[i]= fill;
	}

	for (int i = 0; i <8; i++) {
		// Set page number
		ssd1315_setpos(0, i);
		// Transmit the 129 bytes
		uint32_t ret = i2c1_transmit(SSD1315_ADDR, buf, sizeof buf);
		if (ret != 0) {
			 /* Probably the bus is busy or whatever */
			return SSD1315_ERR;
		}
	}

	return SSD1315_OK;
}

// Put a character on the display
ssd1315_status_t ssd1315_putchar(char ch) {
	uint8_t i;
	uint8_t c;
	uint8_t buf[7] = {0x40, 0};

	// Check range
	if (ch<32 || ch>127) {
		// Not in range
		c = 0x7f-32;
	} else {
		// Scale to correct character
		c = ch-32;
	}
	for (i= 0; i < 6; i++)
	{
		buf[i+1] = ssd1315_font6x8[c*6+i];
	}

	return (i2c1_transmit(SSD1315_ADDR, buf, sizeof buf) == 0) ? SSD1315_OK : SSD1315_ERR;
}

// Put a string on the display
ssd1315_status_t ssd1315_puts(char *s) {

	while (s != NULL && *s) {
		ssd1315_status_t ret = ssd1315_putchar(*s++);
		if (ret != 0) {
			return SSD1315_ERR;
		}
	}

	return SSD1315_OK;
}

// Initialize the SSD1315 with default values
ssd1315_status_t ssd1315_init(void) {

	return (i2c1_transmit(SSD1315_ADDR, ssd1315_init_sequence, sizeof ssd1315_init_sequence) == 0) ? SSD1315_OK : SSD1315_ERR;
}

// Set the contrast (0x00 - 0xFF)
ssd1315_status_t ssd1315_setcontrast(uint8_t contrast) {

	uint8_t buf[] = {0x00, 0x81, contrast};

	return (i2c1_transmit(SSD1315_ADDR, buf, sizeof buf) == 0) ? SSD1315_OK : SSD1315_ERR;
}

